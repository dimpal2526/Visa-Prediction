import streamlit as st
import pandas as pd
import joblib
import numpy as np
from sklearn.metrics import mean_absolute_error, mean_squared_error, r2_score

# Load model
model = joblib.load("final_model.pkl")

st.title(" Visa Processing Time Predictor")

#  SINGLE INPUT
st.header("Single Prediction")

country = st.text_input("Country")
visa_type = st.text_input("Visa Type")
processing_office = st.text_input("Processing Office")
date = st.date_input("Application Date")

if st.button("Predict Single"):

    month = date.month

    input_data = pd.DataFrame([{
        "country": country,
        "visa_type": visa_type,
        "processing_office": processing_office,
        "application_month": month,
        "country_avg": 100,
        "visa_avg": 100
    }])

    result = model.predict(input_data)[0]

    st.success(f"⏱ Estimated Time: {int(result)} days")

#  DATASET UPLOAD
st.header("Bulk Prediction (Upload CSV)")

uploaded_file = st.file_uploader("Upload Visa Dataset", type=["csv"])

if uploaded_file:

    df = pd.read_csv(uploaded_file)

    st.write(" Dataset Preview:")
    st.dataframe(df.head())

    try:
        # Required columns
        required_cols = [
            "country",
            "visa_type",
            "processing_office",
            "application_month",
            "country_avg",
            "visa_avg"
        ]

        missing_cols = [col for col in required_cols if col not in df.columns]

        if missing_cols:
            st.error(f" Missing columns: {missing_cols}")
        else:
            preds = model.predict(df[required_cols])
            df["predicted_days"] = preds

            st.success(" Predictions completed")
            st.dataframe(df.head())


            #  ACCURACY CALCULATION

            if "processing_days" in df.columns:

                y_true = df["processing_days"]
                y_pred = df["predicted_days"]

                mae = mean_absolute_error(y_true, y_pred)
                rmse = np.sqrt(mean_squared_error(y_true, y_pred))
                r2 = r2_score(y_true, y_pred)

                st.subheader(" Model Accuracy on Uploaded Dataset")
                st.write(f"MAE: {mae:.2f}")
                st.write(f"RMSE: {rmse:.2f}")
                st.write(f"R² Score: {r2:.2f}")

            else:
                st.warning(" No 'processing_days' column found → accuracy not calculated")

    except Exception as e:
        st.error(f" Error: {str(e)}")