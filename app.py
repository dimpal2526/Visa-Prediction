import streamlit as st
import pandas as pd
import joblib

model = joblib.load("final_model.pkl")

st.sidebar.title("🌍 Visa AI Dashboard")

page = st.sidebar.radio("Navigation", [
    "Dashboard",
    "Single Prediction",
    "Bulk Prediction",
    "Monitoring",
    "Retrain Model"
])

if page == "Dashboard":
    st.title("📊 Dashboard")

    uploaded_file = st.file_uploader("Upload Dataset", type=["csv"])

    if uploaded_file:
        df = pd.read_csv(uploaded_file)
        st.session_state["data"] = df

        st.success("Dataset uploaded ✅")
        st.dataframe(df.head())

if page == "Dashboard":
    st.title("📊 Dashboard")

    uploaded_file = st.file_uploader("Upload Dataset", type=["csv"])

    if uploaded_file:
        df = pd.read_csv(uploaded_file)
        st.session_state["data"] = df

        st.success("Dataset uploaded ✅")
        st.dataframe(df.head())

if page == "Dashboard":
    st.title("Dashboard")

elif page == "Single Prediction":
    st.title("Single Prediction")

elif page == "Bulk Prediction":
    st.title("📦 Bulk Prediction")

    if "data" in st.session_state:
        df = st.session_state["data"]

        preds = model.predict(df)
        df["prediction"] = preds

        st.dataframe(df)

    else:
        st.warning("Upload dataset in Dashboard first ⚠️")

elif page == "Monitoring":
    st.title("Monitoring")

elif page == "Retrain Model":
    st.title("Retrain Model")

if page == "Dashboard":
    st.title("Dashboard")

elif page == "Bulk Prediction":
    st.title("Bulk Prediction")

elif page == "Monitoring":
    st.title("📈 Monitoring")

    if "data" in st.session_state:
        df = st.session_state["data"]

        preds = model.predict(df)
        st.line_chart(preds)

    else:
        st.warning("Upload dataset first ⚠️")

elif page == "Retrain Model":
    st.title("Retrain Model")

if page == "Dashboard":
    st.title("Dashboard")

elif page == "Bulk Prediction":
    st.title("Bulk Prediction")

elif page == "Monitoring":
    st.title("Monitoring")

elif page == "Retrain Model":
    st.title("🔁 Retrain Model")

    if "data" in st.session_state:
        df = st.session_state["data"]

        if st.button("Retrain"):
            X = df.drop("processing_days", axis=1)
            y = df["processing_days"]

            from sklearn.ensemble import RandomForestRegressor
            new_model = RandomForestRegressor()
            new_model.fit(X, y)

            import joblib
            joblib.dump(new_model, "final_model.pkl")

            st.success("Model retrained ✅")

    else:
        st.warning("Upload dataset first ⚠️")