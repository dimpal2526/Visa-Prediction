import streamlit as st
import pandas as pd
import joblib
import numpy as np
import matplotlib.pyplot as plt

# ================= CONFIG =================
st.set_page_config(page_title="Visa AI System", layout="wide")

# ================= LOGIN SYSTEM =================
if "logged_in" not in st.session_state:
    st.session_state.logged_in = False

def login():
    st.title("🔐 Login")

    user = st.text_input("Username")
    pwd = st.text_input("Password", type="password")

    if st.button("Login"):
        if user == "admin" and pwd == "1234":
            st.session_state.logged_in = True
            st.success("Login successful")
            st.rerun()
        else:
            st.error("Invalid credentials")

if not st.session_state.logged_in:
    login()
    st.stop()

# ================= LOAD MODEL =================
model = joblib.load("final_model.pkl")

# ================= SIDEBAR =================
st.sidebar.title("🌍 Visa AI Dashboard")
menu = st.sidebar.radio("Navigation", [
    "Dashboard",
    "Single Prediction",
    "Bulk Prediction",
    "Monitoring",
    "Retrain Model"
])

# ================= DASHBOARD =================
if menu == "Dashboard":
    st.title("📊 Dashboard")

    col1, col2, col3 = st.columns(3)

    col1.metric("Total Predictions", "120")
    col2.metric("Avg Processing Time", "115 days")
    col3.metric("Model Accuracy", "85%")

# ================= SINGLE =================
elif menu == "Single Prediction":

    st.title("🧾 Single Prediction")

    col1, col2 = st.columns(2)

    with col1:
        country = st.selectbox("Country", ["India","USA","Canada"])
        visa_type = st.selectbox("Visa Type", ["Student","Work","Tourist"])

    with col2:
        office = st.selectbox("Office", ["Delhi","New York","Toronto"])
        date = st.date_input("Date")

    if st.button("Predict"):
        input_data = pd.DataFrame([{
            "country": country,
            "visa_type": visa_type,
            "processing_office": office,
            "application_month": date.month,
            "country_avg": 100,
            "visa_avg": 100
        }])

        result = model.predict(input_data)[0]

        st.success(f"⏱ {int(result)} days")

# ================= BULK =================
elif menu == "Bulk Prediction":

    st.title("📦 Bulk Prediction")

    file = st.file_uploader("Upload CSV")

    if file:
        df = pd.read_csv(file)

        st.dataframe(df.head())

        preds = model.predict(df[[
            "country","visa_type","processing_office",
            "application_month","country_avg","visa_avg"
        ]])

        df["predicted_days"] = preds

        st.success("Done")
        st.dataframe(df.head())

        # Download
        st.download_button("Download CSV",
                           df.to_csv(index=False),
                           "predictions.csv")

# ================= MONITORING =================
elif menu == "Monitoring":

    st.title("📈 Model Monitoring")

    file = st.file_uploader("Upload CSV for monitoring")

    if file:
        df = pd.read_csv(file)

        if "processing_days" in df.columns:

            preds = model.predict(df[[
                "country","visa_type","processing_office",
                "application_month","country_avg","visa_avg"
            ]])

            error = abs(df["processing_days"] - preds)

            st.write("Avg Error:", error.mean())

            fig, ax = plt.subplots()
            ax.plot(error.values)
            st.pyplot(fig)

# ================= RETRAIN =================
elif menu == "Retrain Model":

    st.title("🔄 Retrain Model")

    file = st.file_uploader("Upload training data")

    if file:
        df = pd.read_csv(file)

        if "processing_days" in df.columns:

            from sklearn.ensemble import RandomForestRegressor

            X = df[[
                "country","visa_type","processing_office",
                "application_month","country_avg","visa_avg"
            ]]
            y = df["processing_days"]

            new_model = RandomForestRegressor()
            new_model.fit(X, y)

            joblib.dump(new_model, "final_model.pkl")

            st.success("Model Updated!")