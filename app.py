import streamlit as st
import pandas as pd
import joblib

# ================== PAGE CONFIG ==================
st.set_page_config(
    page_title="Visa AI System",
    page_icon="🌍",
    layout="wide"
)

# ================== CUSTOM UI ==================
st.markdown("""
<style>
.main {
    background-color: #0E1117;
}
.stMetric {
    background-color: #1c1f26;
    padding: 10px;
    border-radius: 10px;
}
</style>
""", unsafe_allow_html=True)

# ================== LOAD MODEL ==================
model = joblib.load("final_model.pkl")

# ================== SIDEBAR ==================
st.sidebar.title("🌍 Visa AI Dashboard")

page = st.sidebar.radio("Navigation", [
    "Dashboard",
    "Single Prediction",
    "Bulk Prediction",
    "Monitoring",
    "Retrain Model"
])

# Dataset status
if "data" in st.session_state:
    st.sidebar.success("✅ Dataset Loaded")
else:
    st.sidebar.warning("⚠️ No Dataset")

# ================== COMMON FUNCTION ==================
def prepare_data(df):
    required_cols = [
        "country",
        "visa_type",
        "processing_office",
        "application_month",
        "country_avg",
        "visa_avg"
    ]

    df = df.copy()

    for col in required_cols:
        if col not in df.columns:
            df[col] = 0

    return df[required_cols]

# ================== DASHBOARD ==================
if page == "Dashboard":
    st.title("📊 Dashboard")

    uploaded_file = st.file_uploader("Upload Dataset", type=["csv"], key="main")

    if uploaded_file:
        df = pd.read_csv(uploaded_file)
        st.session_state["data"] = df

        st.success("Dataset uploaded ✅")
        st.dataframe(df.head())

        # Feature importance
        if hasattr(model, "feature_importances_"):
            st.subheader("📊 Feature Importance")

            features = [
                "country",
                "visa_type",
                "processing_office",
                "application_month",
                "country_avg",
                "visa_avg"
            ]

            importance = pd.DataFrame({
                "Feature": features,
                "Importance": model.feature_importances_
            })

            st.bar_chart(importance.set_index("Feature"))

# ================== SINGLE PREDICTION ==================
elif page == "Single Prediction":
    st.title("🧾 Single Prediction")

    country = st.text_input("Country", "India")
    visa_type = st.text_input("Visa Type", "Student")
    office = st.text_input("Processing Office", "Delhi")
    month = st.number_input("Application Month", 1, 12, 1)
    country_avg = st.number_input("Country Avg", 100)
    visa_avg = st.number_input("Visa Avg", 100)

    if st.button("Predict"):
        input_df = pd.DataFrame([{
            "country": country,
            "visa_type": visa_type,
            "processing_office": office,
            "application_month": month,
            "country_avg": country_avg,
            "visa_avg": visa_avg
        }])

        input_df = prepare_data(input_df)

        pred = model.predict(input_df)[0]

        st.success(f"✅ Estimated Processing Time: {round(pred,2)} days")

# ================== BULK PREDICTION ==================
elif page == "Bulk Prediction":
    st.title("📦 Bulk Prediction")

    if "data" in st.session_state:
        df = st.session_state["data"].copy()

        df_model = prepare_data(df)

        preds = model.predict(df_model)
        df["prediction"] = preds

        st.success("Predictions completed ✅")

        # METRICS
        col1, col2, col3 = st.columns(3)
        col1.metric("Total Records", len(df))
        col2.metric("Avg Days", round(df["prediction"].mean(), 2))
        col3.metric("Max Days", df["prediction"].max())

        # CHARTS
        st.subheader("📊 Prediction Distribution")
        st.bar_chart(df["prediction"])

        st.subheader("📈 Prediction Trend")
        st.line_chart(df["prediction"])

        # FILTER
        country_filter = st.selectbox("Filter by Country", df["country"].unique())
        st.dataframe(df[df["country"] == country_filter])

        # DOWNLOAD
        csv = df.to_csv(index=False).encode("utf-8")
        st.download_button("📥 Download Results", csv, "predictions.csv")

    else:
        st.warning("Upload dataset first ⚠️")

# ================== MONITORING ==================
elif page == "Monitoring":
    st.title("📈 Model Monitoring")

    if "data" in st.session_state:
        df = st.session_state["data"].copy()

        df_model = prepare_data(df)

        preds = model.predict(df_model)

        st.metric("Average Prediction", round(preds.mean(), 2))

        st.line_chart(preds)
        st.bar_chart(preds)

    else:
        st.warning("Upload dataset first ⚠️")

# ================== RETRAIN MODEL ==================
elif page == "Retrain Model":
    st.title("🔁 Retrain Model")

    if "data" in st.session_state:
        df = st.session_state["data"].copy()

        if st.button("Retrain Model"):
            from sklearn.ensemble import RandomForestRegressor

            X = prepare_data(df)
            y = df["processing_days"] if "processing_days" in df.columns else X.iloc[:, -1]

            new_model = RandomForestRegressor()
            new_model.fit(X, y)

            joblib.dump(new_model, "final_model_fixed.pkl")

            st.success("Model retrained successfully ✅")

    else:
        st.warning("Upload dataset first ⚠️")